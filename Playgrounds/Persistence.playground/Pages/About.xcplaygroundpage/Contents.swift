/*
 Sometimes called Local storage, Persistence, Offline storage.
 This is a technique used to store data locally on the device so that the app can function even when the device is not connected to the internet. In another words, Apple provides functionalities for us to store data when our app closes, so that when people open your app again later, they can continue working without interruption.

 These are most commont ways of persistence to store our app data:
 - User default
 - File manager
 - Core Data - SwiftData
 - SQLite
 - Keychain and iCloud
 - Realm
 
 Overall, the choice of offline storage and persistence technique depends on the specific requirements of the app and the type of data that needs to be stored.
*/
