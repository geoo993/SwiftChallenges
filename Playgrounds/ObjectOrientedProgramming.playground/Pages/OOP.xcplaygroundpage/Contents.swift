 Object Oriented Programming
 
 One of the most common part of programming languages (C++, C#, python, Java etc...) is that at their core they all follow a programming paradigm called Object Oriented Programming. It is at the heart of most frameworks you’ll be working with and many years it has been a proven approach for simplifying complex systems, which dates back to the 1950s.
 
 - There are many concepts in Object-oriented programming (OOP) which all stem from these 3 basic fundamental ideas of OOP. They are:
    1) Classes: a class is an extensible program-code-template for creating objects, providing initial values for state (member variables) and implementations of behaviour (member functions, methods).
    2) Properties: classes and instances can have associated values named properties. The usual use of properties are instance properties, which can be of any type (enum, class, struct, array).
    3) Methods/functions: these add behaviour to classes and instances. They are functions that are the behaviour of the objects of a class.

 - These are the first building blocks of OOP and that is often used to highlight what OOP is, which is that OOP represents the concept of “objects” that have data `fields` (attributes that describe the object) and associated procedures known as `methods`. Objects, which are usually instances of `classes`, are used to interact with one another to design applications and computer programs.
 
 - In addition to these, there is three high level concepts that stand out in OOP: Encapsulation, Inheritance, and Polymorphism
 
